#!/usr/bin/env bash
# Generate .versions.env from each pinned submodule's git tag.
#
# The git tag is the single source of truth for a service's version (hatch-vcs
# derives everything from it). This script reads `git describe --tags` in each
# submodule working tree and writes the per-service *_VERSION vars that
# docker-compose.yml interpolates into image tags + the APP_VERSION build arg.
#
# Run from anywhere; paths are resolved relative to the repo root. Output is
# .versions.env (gitignored) at the repo root. Re-run after moving a submodule
# pin so the built image tag matches the code inside it.
set -euo pipefail

REPO_ROOT="$(git -C "$(dirname "${BASH_SOURCE[0]}")" rev-parse --show-toplevel)"
OUT="${REPO_ROOT}/.versions.env"

# submodule path -> compose env var prefix
declare -A SERVICES=(
  [torrent-downloader]=TORRENT_DOWNLOADER
  [medialab-jellyfin]=MEDIALAB_JELLYFIN
  [medialab-orchestrator]=MEDIALAB_ORCHESTRATOR
  [medialab-bot]=MEDIALAB_BOT
)

# The value feeds two consumers with conflicting grammars: the Docker image
# tag (no '+') and SETUPTOOLS_SCM_PRETEND_VERSION (strict PEP 440, so no raw
# git-describe '-N-gHASH' suffix). The intersection:
#   exact tag        -> X.Y.Z
#   N commits ahead  -> X.Y.Z.postN   (commit hash stays recoverable from the
#                                      root repo's submodule pin)
#   dirty tree       -> trailing .dev0
version_of() {
  local path="$1" desc dirty=""
  desc="$(git -C "${REPO_ROOT}/${path}" describe --tags --always --dirty 2>/dev/null | sed 's/^v//')"
  if [[ "${desc}" == *-dirty ]]; then
    dirty=".dev0"
    desc="${desc%-dirty}"
  fi
  if [[ "${desc}" =~ ^(.+)-([0-9]+)-g[0-9a-f]+$ ]]; then
    desc="${BASH_REMATCH[1]}.post${BASH_REMATCH[2]}"
  elif [[ ! "${desc}" =~ ^[0-9] ]]; then
    # No tag reachable: describe fell back to a bare commit hash.
    desc="0.0.0"
  fi
  printf '%s%s\n' "${desc}" "${dirty}"
}

: > "${OUT}"
{
  echo "# Generated by bin/medialab-versions.sh - do not edit by hand."
  echo "# Regenerate after moving a submodule pin: bin/medialab-versions.sh"
  for path in "${!SERVICES[@]}"; do
    printf '%s_VERSION=%s\n' "${SERVICES[$path]}" "$(version_of "$path")"
  done
} > "${OUT}"

echo "Wrote ${OUT}:"
cat "${OUT}"
